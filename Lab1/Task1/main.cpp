#include <iostream>

using namespace std;

int main()
{
    cout << "Mazhar Khan\n";     //New Line
    cout << "\tMazhar Khan\n";   //Tab
    cout << "Mazhar \\Khan\n";   //Backslash
    cout << "\'Mazhar Khan\'\n"; //Single quote
    cout << "\"Mazhar Khan\"\n"; //Double quote
    cout << "Mazhar\b Khan";     //Backspace

    return 0;
}
